function [dLfh]= func_compute_dLfh(q,dq,param)
%%%%%%  func_compute_dLfh.m
%%%%  04/24/20
%%%%
%%%%
%%%%
%Inputs
s=q(1);
delq=q(2);
%%%%
%%%%
dq1=dq(1);
%%%%
%%%%
a21=param(1);
a22=param(2);
a23=param(3);
a24=param(4);
a25=param(5);
a31=param(6);
a32=param(7);
a33=param(8);
a34=param(9);
a35=param(10);
%%%%
%%%%
%%%%
%%%%
dLfh=zeros(2,6);
dLfh(1,1) = -(dq1*(12*a23*s^2 - 24*a24*s^2 + 12*a25*s^2 + 12*a21*(s - 1)^2 - 24*a22*(s - 1)^2 + 12*a23*(s - 1)^2 - 24*a24*s*(s - 1) - 12*a22*s*(2*s - 2) + 24*a23*s*(2*s - 2)))/delq^2;
dLfh(1,2) = 0;
dLfh(1,3) = 0;
dLfh(1,4) = (4*a24*s^3 - 4*a25*s^3 - 4*a21*(s - 1)^3 + 4*a22*(s - 1)^3 + 12*a22*s*(s - 1)^2 - 12*a23*s*(s - 1)^2 + 12*a24*s^2*(s - 1) - 6*a23*s^2*(2*s - 2))/delq;
dLfh(1,5) = 1;
dLfh(1,6) = 0;
dLfh(2,1) = -(dq1*(12*a33*s^2 - 24*a34*s^2 + 12*a35*s^2 + 12*a31*(s - 1)^2 - 24*a32*(s - 1)^2 + 12*a33*(s - 1)^2 - 24*a34*s*(s - 1) - 12*a32*s*(2*s - 2) + 24*a33*s*(2*s - 2)))/delq^2;
dLfh(2,2) = 0;
dLfh(2,3) = 0;
dLfh(2,4) = (4*a34*s^3 - 4*a35*s^3 - 4*a31*(s - 1)^3 + 4*a32*(s - 1)^3 + 12*a32*s*(s - 1)^2 - 12*a33*s*(s - 1)^2 + 12*a34*s^2*(s - 1) - 6*a33*s^2*(2*s - 2))/delq;
dLfh(2,5) = 0;
dLfh(2,6) = 1;
%%%%
%%%%
%%End of code